package com.neusoft.sample.Ctrl.yangkangkang;

public class ChengYuJieLong {
	private String itme;    //成语
	private String pinYin;  //拼音
	private String shiYi;   //成语释义
	private String yuJian;  //语见
	private String liJu;    //例句
	private String jinYiCi; //近义词
	private boolean isPDF;  //是否有近义词p
	private String pdfName; //近义词
	private String fanYiCi; //反义词
	public String getItme() {
		return itme;
	}
	public void setItme(String itme) {
		this.itme = itme;
	}
	public String getPinYin() {
		return pinYin;
	}
	public void setPinYin(String pinYin) {
		this.pinYin = pinYin;
	}
	public String getShiYi() {
		return shiYi;
	}
	public void setShiYi(String shiYi) {
		this.shiYi = shiYi;
	}
	public String getYuJian() {
		return yuJian;
	}
	public void setYuJian(String yuJian) {
		this.yuJian = yuJian;
	}
	public String getLiJu() {
		return liJu;
	}
	public void setLiJu(String liJu) {
		this.liJu = liJu;
	}
	public String getJinYiCi() {
		return jinYiCi;
	}
	public void setJinYiCi(String jinYiCi) {
		this.jinYiCi = jinYiCi;
	}
	public boolean isPDF() {
		return isPDF;
	}
	public void setPDF(boolean isPDF) {
		this.isPDF = isPDF;
	}
	public String getPdfName() {
		return pdfName;
	}
	public void setPdfName(String pdfName) {
		this.pdfName = pdfName;
	}
	public String getFanYiCi() {
		return fanYiCi;
	}
	public void setFanYiCi(String fanYiCi) {
		this.fanYiCi = fanYiCi;
	}
}
