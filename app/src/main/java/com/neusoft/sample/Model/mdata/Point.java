package com.neusoft.sample.Model.mdata;

public class Point {
	float x = 0f;
	float y = 0f;
	int color = 0xffff0000;

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}

	public int getColor() {
		return color;
	}

	public void setColor(int color) {
		this.color = color;
	}
}
